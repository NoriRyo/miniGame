void Map::update()
{
	for (int y = 0; y < kMapSizeX; y++)
	{
		for (int x = 0; x < kMapSizeY; x++)
		{
			if (StageNumber == 0)
			{
				kMapData[y][x] = kMapData1[y][x];
			}
			else if (StageNumber == 1 * 4)
			{
				kMapData[y][x] = kMapData2[y][x];
			}
			else if (StageNumber == 2 * 4)
			{
				kMapData[y][x] = kMapData3[y][x];
			}
			else if (StageNumber == 3 * 4)
			{
				kMapData[y][x] = kMapData4[y][x];
			}
			else if (StageNumber == 4 * 4)
			{
				kMapData[y][x] = kMapData5[y][x];
			}
		}
	}
	
}

